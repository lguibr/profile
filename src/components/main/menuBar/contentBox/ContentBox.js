const ContentBox = props => {
	const { content, classes } = props
	console.log(content)
	const getIcon = contentType => {
		switch (contentType) {
			case "github":
				return gitHubIcon
			case "email":
				return mailIcon
			case "phone":
				return phoneIcon
			case "linkedin":
				return linkedinIcon
			case "whatsapp":
				return whatsappIcon
			default:
				return repoIcon
		}
	}

	if (content.id) {
		content.action = () => {
			if (content.homepage && content.homepage !== "") {
				return window.open(content.homepage)
			} else {
				return window.open(content.html_url)
			}
		}
	}

	const icon = getIcon(content.name)
	if (content.name) {
		return (
			<div onClick={() => content.action()} className={classes.container}>
				<SvgIcon viewBox="0 0 32 32" className={classes.icon}>
					<path fill={icon.color} d={icon.path} />
				</SvgIcon>
				<div className={classes.content}>
					<Typography variant="caption">
						<b>{content.name}</b>
					</Typography>
					<Typography variant="caption">{content.value}</Typography>
					<Typography variant="caption">
						{content.description}
					</Typography>
				</div>
			</div>
		)
	} else {
		return false
	}
}
export default withStyles(styles)(ContentBox)
